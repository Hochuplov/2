import random


class Warrior:
    def __init__(self, name, health=100):
        self.name = name
        self.health = health

    def attack(self, other):
        print(f"{self.name} attacking {other.name}")
        other.health -= 20
        if other.health <= 0:
            print(f"{other.name} Lost the fight!")
            print(f"{self.name} I have won!")
            quit()
        print(f"Ð£ {other.name} remained {other.health} health")


warrior1 = Warrior("Dr. Ration")
warrior2 = Warrior("Aventurine")

while True:
    attacker = random.choice([warrior1, warrior2])
    if attacker == warrior1:
        attacker.attack(warrior2)
    else:
        attacker.attack(warrior1)

